// declaration of variables
let a = true;
let b = false;

console.log(`a: ${a}, b: ${b}`);

// Logical AND operator
console.log(`a && b: ${a && b}`);

// Logical OR operator
console.log(`a || b: ${a || b}`);

// Logical NOT operator
console.log(`!a: ${!a}`);
console.log(`!b: ${!b}`);
