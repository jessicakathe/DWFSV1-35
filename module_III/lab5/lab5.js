// declaration of variables
let x = 100;
let y = 200;

console.log(`x: ${x}, y: ${y}`);

// Less than operator
console.log(`x < y: ${x < y}`);

// Greater than operator
console.log(`x > y: ${x > y}`);

// Equality operator
console.log(`x == y: ${x == y}`);

// Inequality operator
console.log(`x != y: ${x != y}`);

// Strict equality operator
console.log(`x === y: ${x === y}`);

// Strict inequality operator
console.log(`x !== y: ${x !== y}`);

// Less than or equal to operator
console.log(`x <= y: ${x <= y}`);

// Greater than or equal to operator
console.log(`x >= y: ${x >= y}`);

